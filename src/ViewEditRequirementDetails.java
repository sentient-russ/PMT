    public ViewEditRequirementDetails(int projIdIn, int reqIdIn) {
        projIdGlobal = projIdIn;
        reqIdGlobal = reqIdIn;
        initComponents();
        getContentPane().setBackground(new Color(54, 69, 79));
        //Begin Header
        TextTotalExpended.setText(Integer.toString(dataAccess.calcExpendedTotal(projIdIn)));
        ModelProject currentProject = dataAccess.GetProject(projIdIn);        
        TextProjectedHrs.setText(currentProject.projEstimatedHours);
        TextRiskStatus.setText(currentProject.projStatus);
        TextProjId.setText(Integer.toString(projIdIn));        
        ArrayList<ModelRisk> risks = dataAccess.GetRisks(projIdIn);
        for(int i = 0; i <= risks.size() -1; i++){
            if(risks.get(i).riskStatus.equalsIgnoreCase("Un-Resolved")){
                TextRiskStatus.setText("Risks Oustanding");
            }
        }
        //End Header
        //Begin details
        ModelRequirement reqModel = dataAccess.GetRequirement(reqIdIn);
        TextRequirementId.setText(String.valueOf(reqIdIn));
        TextRequirementExpenedHours.setText(String.valueOf(dataAccess.GetRequirementExpendedHours(reqIdIn)));
        TextRequirementEstimatedHours.setText(String.valueOf(dataAccess.GetRequirementEstimatedHours(reqIdIn)));
        ComboBoxRequirementType.setSelectedItem(reqModel.reqType);
        ComboBoxRequirementStatus.setSelectedItem(reqModel.reqStatus);
        TextRequirementDescription.setText(reqModel.reqDescription);
        //End details
    }

