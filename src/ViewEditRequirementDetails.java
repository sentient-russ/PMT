    private void requirementFuncBtnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_requirementFuncBtnSaveActionPerformed
        System.out.println("Add/Update Function");
        String button1InitialState = ComboBoxRequirementType.getSelectedItem().toString();
        String button2InitialState = ComboBoxRequirementStatus.getSelectedItem().toString();
        String description = TextRequirementDescription.getText();

        if(button1InitialState.equalsIgnoreCase("Select type...") || button2InitialState.equalsIgnoreCase("Select status...") || description.equalsIgnoreCase("Enter requirement description...")){
            //do nothing maybe display error
        } else{
            //DefaultTableModel modelTeamMembersTbl = (DefaultTableModel)tableTeamMembers.getModel();
            ModelRequirement updateRequirement = new ModelRequirement();
            updateRequirement.projNumber = projIdGlobal;
            updateRequirement.reqId = reqIdGlobal;
            updateRequirement.reqDescription = TextRequirementDescription.getText();
            updateRequirement.reqType = ComboBoxRequirementType.getSelectedItem().toString();
            updateRequirement.reqStatus = ComboBoxRequirementStatus.getSelectedItem().toString();
            dataAccess.UpdateRequirement(updateRequirement); 
        }
        
    }//GEN-LAST:event_requirementFuncBtnSaveActionPerformed

