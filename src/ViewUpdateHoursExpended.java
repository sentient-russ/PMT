    public ViewUpdateHoursExpended(int projIdIn, int reqIdIn) {
        projIdGlobal = projIdIn;
        reqIdGlobal = reqIdIn;
        initComponents();
        getContentPane().setBackground(new Color(54, 69, 79));
        //begin header
        TextProjId.setText(Integer.toString(projIdIn)); 
        TextTotalExpended.setText(Double.toString(dataAccess.calcExpendedTotal(projIdIn)));            
        TextProjectedHrs.setText(Double.toString(dataAccess.calcProjEstimateTotal(projIdIn)));        
        int qtyRisksOutstanding = 0;        
        ArrayList<ModelRisk> risks = dataAccess.GetRisks(projIdIn);
        for(int i = 0; i <= risks.size() -1; i++){
            if(risks.get(i).riskStatus.equalsIgnoreCase("Un-Resolved")){
                TextRiskStatus.setText("remaining");
                qtyRisksOutstanding += 1;
            }
        }
        TextRiskStatus.setText(qtyRisksOutstanding + " Outstanding");
        //End Header
        ArrayList<ModelTeamMember> resultantMemberList = dataAccess.GetTeamMembers(projIdIn);
        for (int i = 0; i <= resultantMemberList.size() -1; i++) {
            String fName = resultantMemberList.get(i).memberFirstName;
            String lName = resultantMemberList.get(i).memberLastName;
            String fullName = fName + " " + lName;
            ComboBoxMember.addItem(fullName);
        }
        //Begin Table
        tableHoursExpended.scrollBarUpdateTeamMembersTable(jScrollPane3);   
        DefaultTableModel modelExpHoursTbl = (DefaultTableModel)tableHoursExpended.getModel();        
        ArrayList<ModelExpendedHours> resultsList = dataAccess.GetExpendedHours(reqIdGlobal);
        for (int i = 0; i <= resultsList.size() -1; i++) {
                String enteredBy = resultsList.get(i).memberFirstName + " " + resultsList.get(i).memberLastName;
                modelExpHoursTbl.addRow(new Object[]{resultsList.get(i).expId, resultsList.get(i).projNumber, resultsList.get(i).reqNumber,
                enteredBy,resultsList.get(i).expDescription,  resultsList.get(i).expHoursType, resultsList.get(i).expNumHours});
        }
        //End Table      
    }
