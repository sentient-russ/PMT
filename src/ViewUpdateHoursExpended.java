    private void riskFuncBtnAddUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_riskFuncBtnAddUpdateActionPerformed
        DefaultTableModel modelExpHoursTbl = (DefaultTableModel)tableHoursExpended.getModel();        
        ModelExpendedHours transitionalExpHoursRecord = new ModelExpendedHours();
        String ComboBoxWorkInitState = ComboBoxWorkType.getSelectedItem().toString();
        String ComboBoxMemberInitState = ComboBoxMember.getSelectedItem().toString();
        if(ComboBoxWorkInitState.equalsIgnoreCase("Select type...") ||ComboBoxMemberInitState.equalsIgnoreCase("Select member...") || TextDescription.getText().equalsIgnoreCase("Enter work description...")){
            //do nothing except possibly notify user of condition
        }else{
            if(TextExpId.getText().equalsIgnoreCase("Expended Id: New-Entry")){  
               //new member - add
                String[] teamMemberName = ComboBoxMember.getSelectedItem().toString().split(" ",2);
                String firstName = teamMemberName[0];
                String lastName = teamMemberName[1];
                transitionalExpHoursRecord.expId = 0;
                transitionalExpHoursRecord.projNumber = projIdGlobal;
                transitionalExpHoursRecord.reqNumber = reqIdGlobal;
                transitionalExpHoursRecord.expDescription= TextDescription.getText();
                transitionalExpHoursRecord.memberFirstName = firstName;
                transitionalExpHoursRecord.memberLastName = lastName;
                transitionalExpHoursRecord.expHoursType = ComboBoxWorkType.getSelectedItem().toString();
                transitionalExpHoursRecord.expNumHours = Double.parseDouble(TextHoursExpended.getText());
                if(transitionalExpHoursRecord.expDescription.equalsIgnoreCase("Enter work description...") ||
                        transitionalExpHoursRecord.memberFirstName.equalsIgnoreCase("Select") ||
                        transitionalExpHoursRecord.expHoursType.equalsIgnoreCase("Select type...")){
                    //do nothing except send a all information required message to the user.
                } else{
                    dataAccess.InsertExpendedHours(transitionalExpHoursRecord);
                    ArrayList<ModelExpendedHours> resultsList = dataAccess.GetExpendedHours(reqIdGlobal);
                    modelExpHoursTbl.setNumRows(0);
                    for (int i = 0; i <= resultsList.size() -1; i++) {
                            String enteredBy = resultsList.get(i).memberFirstName + " " + resultsList.get(i).memberLastName;
                            modelExpHoursTbl.addRow(new Object[]{resultsList.get(i).expId, resultsList.get(i).projNumber, resultsList.get(i).reqNumber,
                            enteredBy,resultsList.get(i).expDescription,  resultsList.get(i).expHoursType, resultsList.get(i).expNumHours});
                    }
                    TextDescription.setText("Enter work description...");
                    ComboBoxWorkType.setSelectedItem("Select type...");
                    ComboBoxMember.setSelectedItem("Select member...");
                    TextHoursExpended.setText("0");
                    String stringId = "Expended Id: New-Entry";
                    TextExpId.setText(stringId);
                }
            } else {
                //pre exhisting - update
                String[] teamMemberName = ComboBoxMember.getSelectedItem().toString().split(" ",2);
                String firstName = teamMemberName[0];
                String lastName = teamMemberName[1];
                String toSplit = TextExpId.getText();
                String[] splitArray = toSplit.split(": "); 
                transitionalExpHoursRecord.expId = Integer.parseInt(splitArray[1]);
                transitionalExpHoursRecord.projNumber = projIdGlobal;
                transitionalExpHoursRecord.reqNumber = reqIdGlobal;
                transitionalExpHoursRecord.expDescription= TextDescription.getText();
                transitionalExpHoursRecord.memberFirstName = firstName;
                transitionalExpHoursRecord.memberLastName = lastName;
                transitionalExpHoursRecord.expHoursType = ComboBoxWorkType.getSelectedItem().toString();

                transitionalExpHoursRecord.expNumHours = Double.parseDouble(TextHoursExpended.getText());
                ArrayList<ModelExpendedHours> resultsList = dataAccess.UpdateExpendedHoursRecord(transitionalExpHoursRecord);
                modelExpHoursTbl.setNumRows(0);
                for (int i = 0; i <= resultsList.size() -1; i++) {
                        String enteredBy = resultsList.get(i).memberFirstName + " " + resultsList.get(i).memberLastName;
                        modelExpHoursTbl.addRow(new Object[]{resultsList.get(i).expId, resultsList.get(i).projNumber, resultsList.get(i).reqNumber,
                        enteredBy,resultsList.get(i).expDescription,  resultsList.get(i).expHoursType, resultsList.get(i).expNumHours});
                }
                
                
                TextDescription.setText("Enter work description...");
                ComboBoxWorkType.setSelectedItem("Select type...");
                ComboBoxMember.setSelectedItem("Select member...");
                TextHoursExpended.setText("0");
                String stringId = "Expended Id: New-Entry";
                TextExpId.setText(stringId);
            }
        }
    }//GEN-LAST:event_riskFuncBtnAddUpdateActionPerformed

